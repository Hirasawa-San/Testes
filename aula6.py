ad = 4 + 4
print(ad, "Adição", end="\n")

sub = ad - 4
print(sub, "Subtração", end="\n")

mult = 4 * sub
print(mult, "Multiplicação", end="\n")

div = 4 / mult
print(div, "Divisão", end="\n")

div_inteira = 10 // 3 # Divisão que não considera os numeros após o ponto(virgula)
print(div_inteira, "Divisão Inteira", end="\n")

expo = 2 ** 10
print(expo, "Exponenciação", end="\n")

rest_div = 55 % 2 # Resto de divisão
print(rest_div, "Resto de divisão", end="\n")

